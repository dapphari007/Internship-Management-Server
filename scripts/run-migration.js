import { Pool } from 'pg';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import dotenv from 'dotenv';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

dotenv.config();

// Create database connection using production DATABASE_URL
const pool = new Pool({
  connectionString: process.env.DATABASE_URL,
  ssl: { rejectUnauthorized: false }
});

async function runMigration() {
  const client = await pool.connect();
  
  try {
    console.log('ðŸ”„ Running profile completion percentage migration...');
    
    const migrationPath = path.join(__dirname, '..', 'database', 'migrations', '006_add_profile_completion_percentage.sql');
    const migrationSQL = fs.readFileSync(migrationPath, 'utf8');
    
    await client.query('BEGIN');
    await client.query(migrationSQL);
    await client.query('COMMIT');
    
    console.log('âœ… Migration completed successfully!');
    
  } catch (error) {
    await client.query('ROLLBACK');
    console.error('âŒ Migration failed:', error);
    throw error;
  } finally {
    client.release();
    await pool.end();
  }
}

runMigration()
  .then(() => {
    console.log('ðŸŽ‰ Migration process completed!');
    process.exit(0);
  })
  .catch((error) => {
    console.error('ðŸ’¥ Migration process failed:', error.message);
    process.exit(1);
  });
